x = int(input(" Pick a number 1-7 "))

if x == 1:
    print("monday")

if x == 2:
    print("tuesday")

if x == 3:
    print("wednesday")

if x == 4:
    print("thursday")

if x == 5:
    print("friday")

if x == 6:
    print("saturday")

if x == 7:
    print("sunday")

if x >= 8 or x < 1:
    print(" error message, please pick a number 1-7 ")
